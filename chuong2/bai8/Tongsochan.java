package chuong2.bai8;

public class Tongsochan {

	public static void main(String[] args) {
		        int sum = 0;

		        // Tính tổng các số chẵn từ 1 đến 100
		        for (int i = 2; i <= 100; i+=2) {
		                sum += i;
		        }

		        // Hiển thị kết quả
		        System.out.println("Tổng các số chẵn từ 1 đến 100 là: " + sum);
	}

}
