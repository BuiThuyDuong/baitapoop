package chuong2.bai9;

public class Tongsole {

	public static void main(String[] args) {
		        int sum = 0;

		        // Tính tổng các số chẵn từ 1 đến 100
		        for (int i = 1; i < 100; i+=2) {
		                sum += i;
		        }

		        // Hiển thị kết quả
		        System.out.println("Tổng các số lẻ từ 1 đến 100 là: " + sum);
	}

}
